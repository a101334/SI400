    private void btnCadastrarAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAnimalActionPerformed
        // TODO add your handling code here:
       String nomeAnimal = tfNomeAnimal.getText();
       String date = tfNascimentoAnimal.getText();
       String especie = tfEspecie.getText();
       String raca = tfRaca.getText();
       int codPessoa = Integer.parseInt(tfCodPessoa.getText());
       
       Animal animal = new Animal(nomeAnimal, date, especie, raca, codPessoa );
       
       AnimalDAO animalDAO = new AnimalDAO();
       
       animalDAO.cadastrarAnimal(animal);
    }//GEN-LAST:event_btnCadastrarAnimalActionPerformed

